// 6.Unary operator
    console.log("----------------------");
    console.log(30);
    console.log("3.14");
    console.log(typeof "hello");
    console.log(!true);
    console.log(-true);
    console.log(!0);

// 7.Logical operator
    console.log("----------------------");
    console.log(5 > 10 && 2 > 1);
    console.log(3 > 2 || 4 > 5);
    console.log("banana" === "apple" || 10 !== 10);
    console.log(true && true);
    console.log(false || false); 
    console.log(!false);

// 8.Ternary
    console.log("----------------------");
    console.log(5 > 2?"greater":"smaller" );
    console.log(5 == 5? "equal":"notequal");
    console.log(0 === 0?"zero":"notzero");
    console.log("keerthi" === "keerthana"?"same":"different");
    console.log(60 <= 50 ? "Below or equal to 50" : "Above 50");

